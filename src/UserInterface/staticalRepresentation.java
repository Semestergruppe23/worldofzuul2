package UserInterface;

public class staticalRepresentation {
    
    
    static double coordinateX=0;
    static double coordinateY=0;
  
    
    void setX(double x)
    {
        coordinateX=x;
    }
     void setY(double y)
    {
        coordinateY=y;
    }
     double getX()
    {
       return coordinateX;
    }
      double getY()
    {
       return coordinateY;
    }
   
    
}
